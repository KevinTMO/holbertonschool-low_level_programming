#ifndef HOLBERTON_H
#define HOLBERTON_H

#include <stdio.h>

/**
 *_putchar will print a single character
 */

int _putchar(char c);

/**
 *_isupper will check for uppercase characters
 */

int _isupper(int c);

/**
 *_isdigit will check for digits in the program between 0 and 9
 */

int _isgidit(int c);

/**
 *mul will multiply two variables
 */

int mul(int a, int b);

/**
 *print_numbers will print numbers from 0 to 9
 */

void print_numbers(void);

/**
 *print_most_numbers will print numbers from 0 to 9 except for 2 and 4
 */

void print_most_numbers(void);

/**
 *more_numbers will print numbers from 0 to 14 ten times
 */

void more_numbers(void);

/**
 *print_lines will print lines depending on the value of n
 */

void print_line(int n);

#endif
